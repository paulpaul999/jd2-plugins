@HostPlugin(revision = "$Revision: 47484 $", interfaceVersion = 3, names = {}, urls = {})
public class PdfdriveCom extends PluginForHost {
    public PdfdriveCom(PluginWrapper wrapper) {
        super(wrapper);
    }

    @Override
    public String getAGBLink() {
        return "https://www.pdfdrive.com/home/terms";
    }

    private static List<String[]> getPluginDomains() {
        final List<String[]> ret = new ArrayList<String[]>();
        // each entry in List<String[]> will result in one PluginForHost, Plugin.getHost() will return String[0]->main domain
        ret.add(new String[] { "pdfdrive.com" });
        return ret;
    }

    public static String[] getAnnotationNames() {
        return buildAnnotationNames(getPluginDomains());
    }

    @Override
    public String[] siteSupportedNames() {
        return buildSupportedNames(getPluginDomains());
    }

    public static String[] getAnnotationUrls() {
        final List<String> ret = new ArrayList<String>();
        for (final String[] domains : getPluginDomains()) {
            ret.add("https?://(?:www\\.)?" + buildHostsPatternPart(domains) + "/([a-z0-9\\-]+)-(?:e|d)(\\d+)\\.html");
        }
        return ret.toArray(new String[0]);
    }

    /* Connection stuff */
    private static final boolean FREE_RESUME       = true;
    /* 2020-12-01: More chunks possible but let's not stress their servers too much. */
    private static final int     FREE_MAXCHUNKS    = 1;
    private static final int     FREE_MAXDOWNLOADS = 20;

    @Override
    public String getLinkID(final DownloadLink link) {
        final String fid = getFID(link);
        if (fid != null) {
            return this.getHost() + "://" + fid;
        } else {
            return super.getLinkID(link);
        }
    }

    private String getFID(final DownloadLink link) {
        return new Regex(link.getPluginPatternMatcher(), this.getSupportedLinks()).getMatch(1);
    }

    private String getNameURL(final String url) {
        return new Regex(url, this.getSupportedLinks()).getMatch(0);
    }

    @Override
    public AvailableStatus requestFileInformation(final DownloadLink link) throws IOException, PluginException {
        this.setBrowserExclusive();
        br.setFollowRedirects(true);
        br.getPage(buildContentURL(link));
        if (br.getHttpConnection().getResponseCode() == 404) {
            throw new PluginException(LinkStatus.ERROR_FILE_NOT_FOUND);
        } else if (br.toString().length() < 100) {
            /* 2020-12-01: E.g. response "no file." (blank page apart from that.) */
            throw new PluginException(LinkStatus.ERROR_FILE_NOT_FOUND);
        }
        /* Grab temp. name from URL - set final filename on downloadstart. */
        String filename = getNameURL(link.getPluginPatternMatcher()).replace("-", " ");
        if (!link.isNameSet()) {
            /* Fallback */
            link.setName(filename + ".pdf");
        }
        final String[] bookInfo = br.getRegex("class=\"info-green\">([^<>\"]+)<").getColumn(0);
        if (bookInfo.length >= 0) {
            for (String filesizeCandidate : bookInfo) {
                filesizeCandidate = filesizeCandidate.trim();
                if (filesizeCandidate.matches("^\\d+(\\.\\d{1,2})?\\s*[A-Za-z]{1,2}$")) {
                    link.setDownloadSize(SizeFormatter.getSize(filesizeCandidate));
                    break;
                }
            }
        }
        return AvailableStatus.TRUE;
    }

    private String buildContentURL(final DownloadLink link) {
        final Regex linkinfo = new Regex(link.getPluginPatternMatcher(), this.getSupportedLinks());
        final String countryCode = linkinfo.getMatch(0);
        final String fileID = linkinfo.getMatch(1);
        return "https://www." + this.getHost() + "/" + countryCode + "-e" + fileID + ".html";
    }

    @Override
    public void handleFree(final DownloadLink link) throws Exception, PluginException {
        handleDownload(link, FREE_RESUME, FREE_MAXCHUNKS, "free_directlink");
    }

    private void handleDownload(final DownloadLink link, final boolean resumable, final int maxchunks, final String directlinkproperty) throws Exception, PluginException {
        String dllink = checkDirectLink(link, directlinkproperty);
        if (dllink == null) {
            requestFileInformation(link);
            /* This will skip one download step + pre-download-waittime! */
            final String session = br.getRegex("session=([a-f0-9]+)").getMatch(0);
            if (session == null) {
                throw new PluginException(LinkStatus.ERROR_PLUGIN_DEFECT);
            }
            br.getPage("/ebook/broken?id=" + this.getFID(link) + "&session=" + session + "&r=" + new Random().nextInt(100));
            dllink = br.getRegex("\"(/download\\.pdf[^\"]+ext=pdf[^\"]*)\"").getMatch(0);
            if (dllink == null) {
                /* E.g. externally hosted */
                dllink = br.getRegex("href=\"(https?://[^\"]+)\" target=\"_blank\"[^>]*>\\s*Go to PDF").getMatch(0);
            }
            if (StringUtils.isEmpty(dllink)) {
                logger.warning("Failed to find final downloadurl");
                throw new PluginException(LinkStatus.ERROR_PLUGIN_DEFECT);
            }
        }
        dl = jd.plugins.BrowserAdapter.openDownload(br, link, dllink, resumable, maxchunks);
        if (!this.looksLikeDownloadableContent(dl.getConnection())) {
            br.followConnection(true);
            if (dl.getConnection().getResponseCode() == 403) {
                throw new PluginException(LinkStatus.ERROR_TEMPORARILY_UNAVAILABLE, "Server error 403", 60 * 60 * 1000l);
            } else if (dl.getConnection().getResponseCode() == 404) {
                throw new PluginException(LinkStatus.ERROR_TEMPORARILY_UNAVAILABLE, "Server error 404", 60 * 60 * 1000l);
            } else if (br.toString().length() <= 100) {
                /* Text errormessage e.g. 'Preview not available for this file, try downloading instead' --> Broken file/download */
                throw new PluginException(LinkStatus.ERROR_FATAL, br.toString());
            } else {
                throw new PluginException(LinkStatus.ERROR_PLUGIN_DEFECT);
            }
        }
        link.setProperty(directlinkproperty, dl.getConnection().getURL().toString());
        dl.startDownload();
    }

    private String checkDirectLink(final DownloadLink link, final String property) {
        String dllink = link.getStringProperty(property);
        if (dllink != null) {
            URLConnectionAdapter con = null;
            try {
                final Browser br2 = br.cloneBrowser();
                br2.setFollowRedirects(true);
                con = br2.openHeadConnection(dllink);
                if (this.looksLikeDownloadableContent(con)) {
                    if (con.getCompleteContentLength() > 0) {
                        link.setVerifiedFileSize(con.getCompleteContentLength());
                    }
                    return dllink;
                } else {
                    throw new IOException();
                }
            } catch (final Exception e) {
                logger.log(e);
                return null;
            } finally {
                if (con != null) {
                    con.disconnect();
                }
            }
        }
        return null;
    }

    @Override
    public boolean hasCaptcha(DownloadLink link, jd.plugins.Account acc) {
        return false;
    }

    @Override
    public int getMaxSimultanFreeDownloadNum() {
        return FREE_MAXDOWNLOADS;
    }

    @Override
    public void reset() {
    }

    @Override
    public void resetDownloadlink(DownloadLink link) {
    }
}